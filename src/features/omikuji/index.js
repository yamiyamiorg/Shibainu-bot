function buildOmikujiEmbed(fortune, text, username, opts = {}) {
    const yamiMsg = YAMI_MESSAGES[fortune.id] ?? '今日も一日がんばろ🌙';

    const penaltyNote = opts.daikyoPenalty ? '\nまた引いたの…？' : '';

    return new EmbedBuilder()
        .setColor(fortune.color)
        .setTitle(`${fortune.emoji} ${fortune.label}`)
        .setDescription(text)
        .addFields({ name: 'しばいぬより', value: `${yamiMsg}${penaltyNote}`, inline: false })
        .setFooter({ text: `${username} さんの今日の運勢` })
        .setTimestamp();
}

// ─────────────────────────────────────────────
// feature定義
// ─────────────────────────────────────────────

module.exports = {
    name: 'omikuji',
    description: '/omikuji — やみちゃんのおみくじ（全100パターン）',

    enabled() {
        return isFeatureEnabled('omikuji');
    },

    async setup(client) {
        client.on(Events.InteractionCreate, async (interaction) => {
            if (!interaction.isChatInputCommand()) return;
            if (interaction.commandName !== 'omikuji') return;

            try {
                await interaction.deferReply();

                const fortune  = drawFortune();
                const texts    = OMIKUJI_TEXTS[fortune.id] ?? [];
                const text     = texts.length > 0 ? pickRandom(texts) : '今日の運勢はひみつ🌙';
                const username = interaction.member?.displayName
                    ?? interaction.user?.globalName
                    ?? interaction.user?.username
                    ?? '???';

                const guildId = interaction.guildId ?? 'DM';
                const userId = interaction.user.id;
                const recentDaikyoCount = fortune.id === 'daikyo'
                    ? await countRecentDaikyo(guildId, userId, 24 * 60 * 60)
                    : 0;
                const isDaikyoPenalty = fortune.id === 'daikyo' && recentDaikyoCount >= 1;

                await recordOmikujiDraw(guildId, userId, fortune.id);

                const embed = buildOmikujiEmbed(fortune, text, username, {
                    daikyoPenalty: isDaikyoPenalty,
                });
                await interaction.editReply({ embeds: [embed] });

                logger.info('omikuji.drawn', {
                    userId,
                    guildId,
                    fortune: fortune.label,
                    daikyoPenalty: isDaikyoPenalty,
                });
            } catch (err) {
                logger.error('omikuji.error', { err: err?.message });
                const msg = '運勢を引けなかったよ…ごめんね🌙 もう一度試してみて';
                try {
                    if (interaction.deferred || interaction.replied) {
                        await interaction.editReply({ content: msg, embeds: [] });
                    } else {
                        await interaction.reply({ content: msg, ephemeral: true });
                    }
                } catch { /* noop */ }
            }
        });

        logger.info('omikuji.feature.setup.complete');
    },
};
