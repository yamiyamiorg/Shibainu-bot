function drawFortune() {
    const total = FORTUNE_TABLE.reduce((s, f) => s + f.weight, 0);
    let rand = Math.floor(Math.random() * total);
    for (const fortune of FORTUNE_TABLE) {
        if (rand < fortune.weight) return fortune;
        rand -= fortune.weight;
    }
    return FORTUNE_TABLE[0]; // fallback
}

function pickRandom(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

